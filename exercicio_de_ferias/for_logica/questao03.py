# Exercício 03
# Crie um programa que calcule o valor de um terreno retangular, pedindo ao usuário que
# digite a largura e o comprimento do terreno.
# Saída de Dados (terminal):
# Digite a largura do terreno: 10
# Digite o comprimento do terreno: 15
# A área do terreno é: 150.0

largura = float(input("Digite a largura: "))
comprimento = float(input("Digite o comprimento: "))
print("Area = ", comprimento * largura)
